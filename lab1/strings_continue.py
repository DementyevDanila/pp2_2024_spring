b = "abcdefghij"
print(b[2:5]) #starts from 2 ends with 5, but 5th element is not incuded. Also string starts from 0th element

b = "abcdefghij"
print(b[:5]) #starts from 0 

b = "abcdefghij"
print(b[2:]) #starts from 2 and goes till the end

b = "abcdefghij"
print(b[-5:-2]) #counting starts from behind, -5th elemnt is "f" and counter goes to the h(without including "i")